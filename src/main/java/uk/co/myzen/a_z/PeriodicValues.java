package uk.co.myzen.a_z;

public class PeriodicValues {

	private Integer countHalfHours;

	private Float accConsumption;

	private Float accCost;

	public Float getAccConsumption() {
		return accConsumption;
	}

	public void setAccConsumption(Float accConsumption) {
		this.accConsumption = accConsumption;
	}

	public Float getAccCost() {
		return accCost;
	}

	public void setAccCost(Float accCost) {
		this.accCost = accCost;
	}

	public Integer getCountHalfHours() {
		return countHalfHours;
	}

	public void setCountHalfHours(Integer countHalfHours) {
		this.countHalfHours = countHalfHours;
	}
}
